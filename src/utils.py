
#Recode "family/personal history of" codes (start with Z80 to Z87)
z_recode_stems = tuple(['Z'+str(x) for x in range(80,88)])

def Recode_z(df, z_recode_stems):
    r = []
    for code in df['entity_code'].astype(str):
        if code.startswith(z_recode_stems):
            res = df.loc[df['entity_code'] == code,'results'].values[0].split(':::')[1]
            r.append({'code':code, 'res':res})
    return r

def most_common(lst):
    return max(set(lst), key=lst.count)



